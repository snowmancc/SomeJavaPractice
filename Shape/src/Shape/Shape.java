
package Shape;

public class Shape {
    
    public int perimeter;
    public int area;
    
    public Shape() {
        perimeter = 0;
        area = 0;
    }
    
    public int getPerimeter () {
        
        return perimeter;
    }
    
    public int getArea() {
        
        return area;
    }
    
    
}
